    public static Path capture() {
        final MuleLicenseView view = new MuleLicenseView();
        view.getAccessibleContext().setAccessibleDescription(Bundle.MuleLicenseView_Title());
        DialogDescriptor dd = new DialogDescriptor(view, Bundle.MuleLicenseView_UpdateLicenseActionTitle());
        dd.setClosingOptions(new Object[] {
            view.getOKButton(),
            DialogDescriptor.CANCEL_OPTION
        });
        dd.setOptions(new Object[]{
            view.getOKButton(),
            DialogDescriptor.CANCEL_OPTION
        });
        if (view.getOKButton() == DialogDisplayer.getDefault().notify(dd)) {
            return view.getMuleHome();
        } else {
            return null;
        }
    }

