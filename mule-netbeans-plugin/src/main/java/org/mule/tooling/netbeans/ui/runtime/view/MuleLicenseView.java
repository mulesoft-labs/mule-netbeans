    private MuleRuntime getMuleRuntime() {
        return MuleSupport.getMuleRuntime(getMuleHome());
    }

    public Path getMuleHome() {
        return Paths.get(txtLicensePath.getText());
    }

    @Messages({
        "MuleLicenseView_validation_LicensePathNotEmpty=License path can't be empty",
    })
    private void validateInformation() {
        //check mule home
        if (txtLicensePath.getText().trim().length() == 0) {
            lblValidate.setText(Bundle.MuleLicenseView_validation_LicensePathNotEmpty());
            btnOk.setEnabled(false);
            return;
        }
        lblValidate.setText("");
        btnOk.setEnabled(true);
    }

    public javax.swing.JButton getOKButton() {
        return btnOk;
    }

